  private void taiDuLieu(){
      DefaultTableModel dtm = (DefaultTableModel) this.tblbang.getModel();
      dtm.setRowCount(0);
      ArrayList<CuaHangView> list = (ArrayList<CuaHangView>) this.service.getAll();
      for (CuaHangView ch : list) {
        Object [] rowData = {
            ch.getId(),
            ch.getMa(),
            ch.getTen(),
            ch.getDiaChi(),
            ch.getDiaChi(),
            ch.getThanhPho()
        };
        dtm.addRow(rowData);
          
      }
  }
